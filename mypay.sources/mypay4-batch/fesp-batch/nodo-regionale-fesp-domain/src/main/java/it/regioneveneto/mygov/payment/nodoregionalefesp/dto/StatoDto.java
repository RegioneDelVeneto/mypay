package it.regioneveneto.mygov.payment.nodoregionalefesp.dto;

public class StatoDto {

	private String cod;
	private String de;

	public StatoDto() {
		super();
	}

	public String getCod() {
		return cod;
	}

	public void setCod(String cod) {
		this.cod = cod;
	}

	public String getDe() {
		return de;
	}

	public void setDe(String de) {
		this.de = de;
	}

}
