package it.regioneveneto.mygov.payment.mypay4.logging;

import lombok.extern.slf4j.Slf4j;
import org.slf4j.MDC;
import org.springframework.core.Ordered;
import org.springframework.core.annotation.Order;
import org.springframework.stereotype.Component;
import org.springframework.web.filter.OncePerRequestFilter;

import javax.servlet.FilterChain;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

@Component
@Order(Ordered.HIGHEST_PRECEDENCE)
@Slf4j
public class MDCClearFilter extends OncePerRequestFilter {

  @Override
  protected void doFilterInternal(HttpServletRequest request, HttpServletResponse response, FilterChain filterChain) throws ServletException, IOException {
    //since we don't clear the MDC context when completing a request
    // (beacuse exception may be handled in DispatcherServlet and we don't wont to lose MDC at that stage)
    // we need to clear it as soon as a new request will start execution (it will be injected later by JwtRequestFilter)
    MDC.clear();
    filterChain.doFilter(request, response);
  }
}
